     public void keyBinding() {
        // Acci√≥n para aplicar los filtros cuando se presiona ENTER
        Action login = new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                bInicio.doClick(); // Simula un click en aplicar filtro
            }
        };

        // A√±adir keybinding para la tecla ENTER para aplicar los filtros
        rootPane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0), "LOGIN");
        rootPane.getActionMap().put("LOGIN", login); // Fixed: rootpane -> rootPane

        requestFocusInWindow();
        setFocusable(true);
    }

    private void agregarJavaHelp() {
        try {
            URL hsURL = getClass().getClassLoader().getResource("help/help_set.hs");

            HelpSet helpset = new HelpSet(null, hsURL);
            HelpBroker hb = helpset.createHelpBroker();

            hb.enableHelpKey(this.getContentPane(), "MainView2", helpset);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

